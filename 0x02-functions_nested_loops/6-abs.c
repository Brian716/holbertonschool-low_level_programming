#include "holberton.h"

/**
 * _abs - detects a lowercase or uppercase character that is used.
 * _putchar - writes the character c to stdout
 * @c: The character to print
 *
 * Return: On success 1.
 * On error, -1 is returned, and errno is set appropriately.
 */

int _abs(int c)
{
int result;
if (c < 0)
{
result = c * -1;
}
else
{
result = c;
}
return (result);
}
